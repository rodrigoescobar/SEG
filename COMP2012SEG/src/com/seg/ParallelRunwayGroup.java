package com.seg;

/* This class is here to represent a group of parallel runways*/
public class ParallelRunwayGroup {
	
	public Runway left;
	public Runway centre;
	public Runway right;
	
	public void addLeft(Runway runway)
	{
		left = runway;
	}
	public void addCentre(Runway runway)
	{
		centre = runway;
	}
	public void addRight(Runway runway)
	{
		right = runway;
	}
}